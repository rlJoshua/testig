<?php


class Calculator
{
    public function add(float $x, float $y) {
        return $x + $y;
    }

    public function sub(float $x, float $y) {
        return $x - $y;
    }

    public function mul(float $x, float $y) {
        return $x * $y;
    }

    public function div(float $x, float $y) {
        return $x / $y;
    }
}